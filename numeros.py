firstNumber = float(input('Primeiro número: '))
secondNumber = float(input('Segundo número: '))

# O primeiro valor vezes o segundo valor
firstSum = firstNumber + secondNumber
print(f'{firstNumber} + {secondNumber} = {firstSum}')

# A soma do segundo número com a metade do primeiro número
half = firstNumber / 2
secondSum = secondNumber + half
print(f'{secondNumber} + {half} = {secondSum}')

# A diferença do primeiro número com o segundo
difference = firstNumber - secondNumber
print(f'{firstNumber} - {secondNumber} = {difference}')

# O valor negativo do segundo número
negative = secondNumber * -1
print(negative)

